// Прочитати з клавіатури ціле число, яке представляє собою рік. Визначити, чи прочитаний рік є високосний.
// P.S. Рік є високосний, якщо ділиться на 4, але не є високосним, якщо ділиться на 100,
// але все таки є високосним, якщо ділиться на 400.

#include <iostream>

using namespace std;

int main() {
    int year;
    cin >> year;
    cout << year << (((!(year % 4) && year % 100) || !(year % 400)) ? " високосний" : " не високосний") << endl;
    return 0;
}